              ถ้ายังไม่มีอะไรให้ย้อน ปุ่มจะจางและกดไม่ได้ (ไม่ใช่กดแล้วเงียบ)</li>
            <li><strong>ไม่ต้องหาปุ่มเอง</strong> — ในโหมดแก้มี<strong>แถวปุ่มสัญลักษณ์</strong>ครบทุกตัว
