            <li><strong>ย้อน / ทำซ้ำ</strong> — พิมพ์ผิดกดย้อนได้เสมอ: ปุ่ม <strong>ย้อน</strong> กับ <strong>ทำซ้ำ</strong>
              อยู่ซ้ายสุดของแถบ (คีย์ลัด <code>Ctrl+Z</code> · <code>Ctrl+Y</code> หรือ <code>Ctrl+Shift+Z</code>) ·
