            <li><strong>สัญลักษณ์จังหวะ พิมพ์บนแผ่นได้เลย</strong> (คอมพิวเตอร์) —
              <code>_</code> เขบ็ต (กดซ้ำ = 1 ชั้น → 2 ชั้น → ไม่มี) ·
              <code>.</code> จุดเพิ่มความยาว (กดซ้ำ = 1 จุด → 2 จุด → ไม่มี) ·
              <code>~</code> โยงเสียง · <code>^</code> ยืดเสียง (fermata) ·
              <code>-</code> ลากเสียงอีกหนึ่งจังหวะ · <code>( )</code> เอื้อน · <code>{ }</code> สามพยางค์ ·
              <code>|</code> เส้นกั้นห้อง · <code>#</code> ชาร์ป · <code>b</code> แฟลต ·
              <code>n</code> เนเชอรัล · <code>'</code> สูงขึ้นหนึ่งช่วงเสียง
              <br /><span class="muted">พิมพ์สลับลำดับได้ ระบบจัดเรียงให้เองตามมาตรฐาน</span></li>
            <li><strong>เล่นซ้ำ และ "จบรอบ 1. / 2."</strong> — ที่แถบบนของโหมดแก้:
              ปุ่ม <strong>เล่นซ้ำ</strong> (<code>‖: :‖</code>) คลุมบรรทัดที่กำลังแก้ให้เล่นสองเที่ยว ·
              ปุ่ม <strong>จบรอบ</strong> ข้าง ๆ กัน ทำให้บรรทัดนั้นเป็น<strong>ห้องจบ</strong> — กดหนึ่งครั้ง = จบรอบ 1
              (เล่นเฉพาะเที่ยวแรก) · กดอีกครั้ง = จบรอบ 2 (เล่นเฉพาะเที่ยวซ้ำ) · กดอีกครั้ง = เอาออก
              <br /><span class="muted">อยากให้จบรอบเป็นราย<strong>ห้อง</strong> (บรรทัดเดียวมีทั้ง 1. และ 2.)
              แตะโน้ตในห้องนั้น → ปุ่ม <strong>⋯</strong> → ช่อง "ห้องจบ" · แผ่นเพลงจะพิมพ์เลข 1. / 2. ให้
              และตอนกดฟัง เสียงจะเล่นตามลำดับจริง</span></li>
            <li><strong>ให้บางข้อวนไม่เหมือนกัน</strong> — ทำนองเดียวใช้ได้หลายข้อ แต่ถ้า
              <strong>บางข้อวนต่างจากข้ออื่น</strong> (เช่นข้อ 1 ร้องซ้ำท่อน แต่ข้อ 2-3 ร้องผ่านครั้งเดียว)
              ไม่ต้องคัดลอกทำนอง — เปิดข้อนั้นแล้วกด <strong>"การวนซ้ำของข้อนี้"</strong> ใต้หัวข้อ ตั้งได้:
              <br /><span class="muted"><strong>ข้อนี้ไม่วนซ้ำ</strong> (ร้องผ่านครั้งเดียว) ·
              <strong>เล่นซ้ำกี่รอบ</strong> (เฉพาะข้อนี้) · <strong>เข้าห้องจบชุดที่ N</strong> ·
              <strong>ไม่ต้องร้องรับหลังข้อนี้</strong> — ข้อที่ไม่ตั้งอะไรจะวนตามทำนองปกติ ·
              ถ้าเผลอลบเครื่องหมายที่ข้ออื่นอ้างถึง ระบบจะเตือนและร้องตามทำนองแทน ไม่เล่นมั่ว</span></li>
            <li><strong>รู้ตลอดว่างานถูกเก็บหรือยัง</strong> — ในโหมดแก้มีแถบบนสุดบอก
              <strong>"ยังไม่บันทึก"</strong> หรือ <strong>"บันทึกแล้ว ✓"</strong> พร้อมปุ่มบันทึก ·
              ทีมงานที่เข้าสู่ระบบ = <strong>บันทึกร่าง</strong> (เก็บเข้าเซิร์ฟเวอร์เป็นร่าง ยังไม่ทับเพลงที่เผยแพร่) ·
              คนที่ยังไม่ได้เข้าสู่ระบบ = <strong>บันทึกเป็นไฟล์</strong> (JSON เก็บไว้ในเครื่อง แล้วอัปโหลดกลับได้) ·
              ถ้าเผลอปิดหรือรีเฟรช เว็บจะถามให้<strong>กู้คืนงานที่ค้างไว้</strong>ตอนเปิดเพลงนั้นอีกครั้ง</li>
          </ul>
          เนื้อร้องทุกข้อที่ใช้ทำนองเดียวกันจะขยับตามให้เอง คำไม่เหลื่อม · <strong>คอร์ด:</strong> แตะโน้ต →
          ปุ่ม <strong>คอร์ด</strong> เลือกคอร์ด (หรือ "ไม่มีคอร์ด" เพื่อลบคอร์ดโดยไม่แตะโน้ต) ·
          <strong>คอร์ดไหนไม่มีในรายการ พิมพ์เองได้</strong> ในช่องด้านบนแล้วกด <code>Enter</code>
          (เช่น <code>F#m7b5</code> · <code>Cmaj7</code> · <code>Dsus4</code> · <code>G/B</code>) —
          ถ้าพิมพ์ผิดจนอ่านไม่ออก ระบบจะไม่ใส่ให้ และบอกให้แก้ตรงนั้นเลย ·
          ลบโน้ตหมดห้อง คอร์ดหายเอง · ออกจากโหมดแก้ด้วยปุ่ม <strong>✓ เสร็จ</strong> ที่แถบบนของโหมดแก้
          <br /><span class="muted"><strong>แถบเครื่องมืออยู่ล่างจอเสมอ ไม่ทับแผ่นเพลง</strong> —
          แผ่นเพลงเลื่อนอยู่ในกรอบของตัวเอง แถบเครื่องมือเกาะอยู่ใต้กรอบนั้น จึงไม่มีปุ่มไหนมาบังคำร้องที่กำลังพิมพ์
          และปุ่มอยู่ที่เดิมตลอดไม่ขยับหนี · <strong>บนคอมพิวเตอร์</strong> ใช้คีย์บอร์ดตรง ๆ: เลข <strong>1–7</strong> = โน้ต ·
          <strong>#</strong> = ชาร์ป · <strong>b</strong> = แฟลต · ปุ่ม <strong>สูง/ต่ำ</strong> · <strong>แทรก/ทับ</strong>
          และปุ่ม <strong>? วิธีใช้</strong> (รวมคีย์ลัดทั้งหมด) อยู่บนแถบนั้น — <strong>ครั้งแรกที่เข้าโหมดแก้ วิธีใช้จะเปิดให้เอง</strong>
          ครั้งต่อ ๆ ไปจะพับเก็บไว้ กดปุ่ม ? เมื่อไรก็เปิดดูได้ · <strong>บนมือถือ</strong> แตะแล้วคีย์บอร์ดเครื่องเด้งขึ้นมา
          (แป้นเลขสำหรับโน้ต · แป้นไทยสำหรับเนื้อ) แถบเครื่องมือ (ลูกศร · สูง/ต่ำ · ♯♭ · แทรก/ทับ) ขยับขึ้นมาอยู่เหนือคีย์บอร์ดให้เอง ·
          ใครก็แก้ได้ จะเก็บถาวรเข้าคลังค่อยเข้าสู่ระบบ</span></li>
        <li><strong>กดแท็บด้านบน (ฝึกร้อง · แผ่นเพลง · แก้ไข) ตอนอยู่ในโหมดแก้ = ออกจากโหมดแก้</strong> —
          ถ้ายังมีงานที่<strong>ยังไม่บันทึก</strong> เว็บจะถามก่อนเสมอ กด "ยกเลิก" แล้วบันทึกก่อนได้ ·
          ถ้าบันทึกแล้วจะออกให้เลยไม่ถามซ้ำ</li>
        <li><strong>ดาวน์โหลด / พิมพ์</strong> — กดปุ่มลูกศรลงบนแถบเครื่องเล่น เลือกได้ 3 แบบ:
          <strong>พิมพ์ / บันทึก PDF (A4)</strong> · <strong>ข้อมูลเพลง (JSON)</strong> ·
          และ<strong>ไฟล์เสียง MP3</strong> (เสียงเปียโนจริงตามคีย์และความเร็วที่ตั้งไว้)
          <br>เวลาพิมพ์ เพื่อให้แผ่นสวยเรียบร้อย ในกล่องพิมพ์ให้ <strong>ปิด "หัวกระดาษและท้ายกระดาษ" (Headers and footers)</strong> —
          เว็บใส่ชื่อเพลง ชื่อเว็บ เลขหน้า และวันที่ให้เองครบแล้ว</li>
      </ul>
      <p class="muted" style="margin-bottom: 0">
        ทุกเพลงในคลังผ่านการตรวจจากทีมงานแล้ว (ทีมงานที่เข้าสู่ระบบจะเห็นป้าย
        <span class="verified-eg"><Icon name="badge-check" :size="14" />✓ ตรวจแล้ว</span> กำกับไว้ว่าเพลงไหนตรวจเสร็จ)
      </p>

      <h3 id="howto-studio" class="howto-h"><Icon name="file-plus" :size="22" class="howto-ico" />คีย์เพลง (ห้องทำเพลง)</h3>
      <p>
        เริ่มเพลงใหม่ได้ทันทีจากปุ่ม <strong>＋ สร้างเพลงใหม่</strong> — บนคอมพิวเตอร์อยู่มุมขวาบนของแถบ
        บนมือถือเป็น<strong>ปุ่มลอยมุมขวาล่าง</strong>ของหน้ารายการเพลง (หรือเปิดจากเมนู ☰ ก็ได้) —
        กดแล้วจะเปิดแผ่นเพลงเปล่าให้คีย์ได้เลย โดยไม่ต้องเปิดเพลงเก่าก่อน
      </p>
      <ul>
        <li>เพลงแบ่งเป็น <strong>บรรทัด → ห้อง → ช่อง</strong>: 1 ช่องเล็ก = โน้ต 1 ตัว = 1 คำ
          กด Enter หรือเว้นวรรคเพื่อไปช่องถัดไป กดลูกศรซ้าย-ขวาเพื่อเลื่อน
          <br /><span class="muted">ยกเว้น<strong>เอื้อน</strong> (ร้องคำเดียวหลายโน้ต): พิมพ์โน้ตติดกันในช่องเดียว
          ไม่เว้นวรรค แล้วครอบวงเล็บ เช่น <code>(65)</code> — ดู<a href="#/notation#rhythm">คู่มือทำเพลง</a></span></li>
        <li>ใช้<strong>แผงปุ่มสัญลักษณ์</strong>ที่แถบล่างจอ (เลื่อนไปไหนก็ตามไปด้วย):
          แตะช่องโน้ตก่อน แล้วจิ้มสัญลักษณ์ที่ต้องการ — ชาร์ป/แฟลต จิ้มก่อนหรือหลังพิมพ์เลขก็ได้</li>
        <li><strong>นำทางเร็วในเพลง</strong> — ที่แถบล่างจอมี<strong>ปุ่มลูกศร</strong>ให้ข้ามไปมาได้เลย:
          <strong>◀ ▶</strong> โน้ตก่อนหน้า/ถัดไป · <strong>⏮ ⏭</strong> ข้ามทั้งห้อง ·
          <strong>▲ ▼</strong> ขึ้น/ลงบรรทัด — แตะได้ทั้งที่ยังพิมพ์อยู่ ไม่ต้องปิดแป้นพิมพ์
          <br /><span class="muted">ใช้คอมพิวเตอร์ (มีคีย์บอร์ด): กด <code>Tab</code> ไปโน้ตถัดไป ·
          <code>Ctrl</code>+ลูกศร ข้ามห้อง/บรรทัด (Windows/Linux) — เครื่อง Mac ให้ใช้ <code>Tab</code>
          หรือปุ่มลูกศรบนจอแทน (Mac กันปุ่ม Ctrl+ลูกศรไว้สลับหน้าจอ)</span></li>
        <li><strong>ใส่คอร์ดแล้วกด Enter ยืนยันได้เลย</strong> — พิมพ์ชื่อคอร์ดในช่องคอร์ด
          แล้วกด <code>Enter</code> เพื่อยืนยัน หรือ <code>Esc</code> เพื่อยกเลิก (ไม่ต้องเอาเมาส์ไปคลิก)</li>
        <li>ระบบ<strong>นับจังหวะให้ทุกห้อง</strong> — ถ้าขึ้น ❌ แปลว่าโน้ตในห้องนั้นยังไม่ครบ
          หรือเกินจังหวะของเพลง (ห้องแรกของท่อนอาจไม่เต็มห้องได้ เป็นเรื่องปกติ)</li>
        <li>กด <strong>▶ ฟังบรรทัดนี้</strong> เพื่อเช็คด้วยหูว่าคีย์ถูกไหม —
          ตอนเล่น ห้องที่กำลังดังจะสว่างขึ้นและจอเลื่อนตามให้เอง</li>
        <li>พลาดตรงไหน กด <strong>↩ เลิกทำ</strong> (หรือ Ctrl+Z) ย้อนกลับได้ทีละขั้น</li>
        <li>ถ้าหนังสือตัดห้องเดียวขึ้นบรรทัดใหม่ (เช่นก่อนท่อนฮุก) ให้ติ๊ก
          <strong>⤷ ต่อห้องจากบรรทัดก่อน</strong> ที่บรรทัดใหม่ —
          ระบบจะนับจังหวะสองท่อนนั้นรวมเป็นห้องเดียวให้ถูกต้อง</li>
        <li>ทีมงานที่เข้าสู่ระบบแล้ว: กด "บันทึกร่าง" เก็บงานไว้ก่อน แล้ว "ส่งตรวจ"
          เมื่อพร้อม — เพลงจะขึ้นเว็บเมื่อผู้ตรวจอนุมัติ</li>
        <li><strong>ไม่มีบัญชีก็ช่วยได้</strong> — ทำเพลงเสร็จ ส่งให้ทีมนำขึ้นคลังได้ 4 ขั้น:
          <ol>
            <li>คีย์เพลงให้เสร็จ</li>
            <li>กด <strong>"ดาวน์โหลด JSON"</strong> — ได้ไฟล์สำเนางานของคุณ
              (เว็บไม่ได้เก็บงานไว้ให้ เปิดกลับมาแก้ต่อได้ด้วย "อัปโหลด JSON")</li>
